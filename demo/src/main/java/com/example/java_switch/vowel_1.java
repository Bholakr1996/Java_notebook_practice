package com.example.java_switch;

public class vowel_1 {
    public static void main(String[] args) {
        char ch = 'B';
        if(ch>=65 && ch<=90);
        switch (ch){
            default:
                System.out.println("charater is consonant");
                break;
            case 'A': case 'E': case 'I': case 'O': case 'U' :
                System.out.println("character is vowel ");
        }
        }

    }
